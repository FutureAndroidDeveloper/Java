package by.bntu.fitr.povt.bedouins.javalabs.lab7.lab0701.view;

public class Printer {
    public static void outputToConsole(int value) {
        System.out.println(value);
    }

    public static void outputToConsole(double value) {
        System.out.println(value);
    }

    public static void outputToConsole(String str) {
        System.out.print(str);
    }

    public static void outputToConsole(String str, int value) {
        System.out.println(str + value);
    }

    public static void outputToConsole(String str, double value) {
        System.out.println(str + value);
    }
}
